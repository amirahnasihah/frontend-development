const assert = require('assert');

// Task: Complete the function that takes in a string and return the reversed string
// E.g. input = "Hello", output should be "olleH"

const reverse = (inputString) => {
  // Add your code here
  let result = "";
  
  for(let i = inputString.length - 1; i >= 0; i--) {
    result += inputString[i];
  }
  return result;
}

// DO NOT MODIFY CODE BELOW
// Test Cases
assert.strictEqual(reverse("Hello"), "olleH", "Test case 1 output is wrong.")
assert.strictEqual(reverse("Talentlabs"), "sbaltnelaT", "Test case 2 output is wrong.")
assert.strictEqual(reverse(""), "", "Test case 3 output is wrong.")
